export function getRandomWord(excluding: string[] = []): string {
    const available = THAI_WORDS.filter(w => !excluding.includes(w));
    if (available.length === 0) {
        // If somehow we run out, just pick from all
        return THAI_WORDS[Math.floor(Math.random() * THAI_WORDS.length)];
    }
    return available[Math.floor(Math.random() * available.length)];
}

export function assignWords(playerIds: string[]): Record<string, string> {
    const assignments: Record<string, string> = {};
    const usedWords: string[] = [];
    for (const playerId of playerIds) {
        const word = getRandomWord(usedWords);
        assignments[playerId] = word;
        usedWords.push(word);
    }
    return assignments;
}
